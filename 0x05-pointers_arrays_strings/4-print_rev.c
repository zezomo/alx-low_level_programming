#include "main.h"
#include <stdio.h>

/**
 * print_rev - check the codefawdf fa fawfdw fa fwf f fwaffawf
 *
 * @s: adfwfw afgawf wadfa f wfaf wfdaw wd dawd eaegesgegeg
 *
 * Return: Always 0.
 */

void print_rev(char *s)
{
	int i = 0;

	while (s[i])
	{
		i++;
	}
	while (i--)
	{
		_putchar(s[i]);
	}
	_putchar('\n');
}
