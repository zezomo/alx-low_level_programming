/**
 * swap_int - check the codefawdf fa fawfdw fa fwf f fwaffawf
 *
 * @a: adfwfw afgawf wadfa f wfaf wfdaw wd dawd eaegesgegeg
 *
 * @b: hhs ege g e  aefhgegwsg sghegsgae ghhaeghaeggeageg
 *
 * Return: Always 0.
 */

void swap_int(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;

}
